package com.abhi.sqlitehelper;

public class CustomUserData {

    String name, phone;

    public CustomUserData() {
    }

    public CustomUserData(String name, String phone) {
        this.name = name;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
